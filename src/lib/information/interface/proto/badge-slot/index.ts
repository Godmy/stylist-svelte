/** Слот бейджа — счётчик, точка, метка статуса */
export interface IBadgeSlot {
	/** Текст или число бейджа */
	badge?: string | number;
	/** Числовой счётчик (например, уведомления) */
	count?: number;
	/** Показывать маленькую точку без текста */
	dot?: boolean;
	/** Принудительно показать бейдж, даже при нулевом значении */
	showBadge?: boolean;
}
